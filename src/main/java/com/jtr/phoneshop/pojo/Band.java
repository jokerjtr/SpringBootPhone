package com.jtr.phoneshop.pojo;

/**
 *
 */
public class Band {
    private Integer bandid;
    private String bandname;
    private String banden;
    private String bandurl;

    public Integer getBandid() {
        return bandid;
    }

    public void setBandid(Integer bandid) {
        this.bandid = bandid;
    }

    public String getBandname() {
        return bandname;
    }

    public void setBandname(String bandname) {
        this.bandname = bandname;
    }

    public String getBanden() {
        return banden;
    }

    public void setBanden(String banden) {
        this.banden = banden;
    }

    public String getBandurl() {
        return bandurl;
    }

    public void setBandurl(String bandurl) {
        this.bandurl = bandurl;
    }
}
